# Data Types in Python

# Objects & Mutability

# Everything in Python is an object, and each object has a type.
# Some objects are mutable (can be changed), while others are immutable (cannot be changed).
# Examples of mutable objects: lists, dictionaries, sets
# Examples of immutable objects: integers, floats, strings, tuples. 


sugar_amount = 2
print(f"Initial_Sugar Amount: {sugar_amount}")  # Output: <class 'int'>

sugar_amount = 12
print(f"Second_Sugar Amount: {sugar_amount}")  # Output: <class 'int'>

print(f"ID of 2 :{id(2)}")
print(f"ID of 12 :{id(12)}")


